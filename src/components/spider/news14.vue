            <h1 class="arc-tit">奔驰决定退出DTM 转投电动方程式赛车</h1>
            <div class="post-info clearfix">
                <p class="post-info-s clearfix">
                    <span class="source">改装赛事</span>
                    <span class="time">时间：2017年7月25日</span>
                </p>
            </div>
            <div class="arc-body mt10 clearfix">
                <p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">7月24号，<a href="/car/0_0-0.0_0.0-0-123-0-0-0-0-1/" style="color:#157efb;" title="奔驰" target="_blank">奔驰</a>汽车发布公告，决定在2018赛季之后退出德国房车大师赛（DTM）的角逐，并确认，将参加2019/20赛季的电动方程式Formula&nbsp;E比赛。</span> 
</p>
<p style="text-align:center;">
	<img alt="跑车网" src="http://img.lscfan.com/Pictures/Article/2017_07/20170725144027_5275-b.jpg"> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">作为全球最顶级房车赛事的DTM，奔驰自其成立之初（2000年）就参与了这项赛事，并对其发展起到了举足轻重的作用。同时，奔驰也因为在DTM中的卓越表现，为他们带来了无数的荣誉。在过去的十多年的比赛中（2000-2016赛季），奔驰一共获得了七次年度车手冠军和九次年度厂商杯冠军，可以说，奔驰一直伴随着这项运动成长。如今要退出赛事，可见也是权衡再三的结果。</span> 
</p>
<p style="text-align:center;">
	<img alt="跑车网" src="http://img.lscfan.com/Pictures/Article/2017_07/20170725144027_8381-b.jpg"> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">而对于加入Formula&nbsp;E，奔驰其实在去年就已经展现出了浓厚的兴趣，尤其是其在DTM的对手<a href="/car/0_0-0.0_0.0-0-102-0-0-0-0-1/" style="color:#157efb;" title="奥迪" target="_blank">奥迪</a>和<a href="/car/0_0-0.0_0.0-0-103-0-0-0-0-1/" style="color:#157efb;" title="宝马" target="_blank">宝马</a>（官方合作伙伴，为Formula&nbsp;E提供安全车）加入许久并收获颇丰之后，奔驰更是蠢蠢欲动。</span> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">在全球机动车电气化的大背景下，Formula&nbsp;E似乎更能帮助厂商展现出未来发展方向和科技水平。与此同时，相比传统的内燃机汽车运动，Formula&nbsp;E是像一个令人兴奋的初创公司，它提供了一个更新的平台，并可以帮助促进当前和未来电动技术的发展。而Formula&nbsp;E将所有比赛的赛道选择在城市中间，让市民几乎可以免费的看到比赛，也比封闭的赛车场更加亲民。</span> 
</p>
<p style="text-align:center;">
	<img alt="跑车网" src="http://img.lscfan.com/Pictures/Article/2017_07/20170725144028_0666-b.jpg"> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">除此之外，奔驰在未来发展重心也是他们决定退出DTM，加入Formula&nbsp;E的重要原因之一。新能源汽车战略现在是奔驰目前非常关注的核心领域，也是奔驰在未来努力的发展方向，在未来的5到7年内，奔驰将会在这个领域内投入100亿欧元（约775亿人民币），并且希望到2025年左右，奔驰会有15%到25%的纯电动车型。</span> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">在去年的巴黎车展，奔驰正式推出了Generation EQ电动车，其量产车型有望在2020年推出。作为奔驰布局新能源市场的重要产品，让Formula&nbsp;E成为其重要的宣传手段也是奔驰的考虑之一，毕竟这项比赛会在全球各大城市进行十几场的比赛，当地政府、明星、各大企业都是这项赛事宣传的有力保障。</span> 
</p>
<p>
	<br>
</p>
<div style="text-align:center;">
	<img alt="跑车网" src="http://img.lscfan.com/Pictures/Article/2017_07/20170725144028_2053-b.jpg"> 
</div>
<span style="font-size:16px;font-family:'Microsoft YaHei';"> 
<div style="text-align:center;">
	<span style="line-height:1.5;">奔驰&nbsp;Formula&nbsp;E赛车假想图</span><span style="line-height:1.5;"></span> 
</div>
</span> 
<p>
	<br>
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">奔驰加入Formula&nbsp;E之后，将成为第五个加入这项运动赛事的“传统”汽车厂商，另四个为：奥迪（Abt Schaeffler Audi Sport team）、雪铁龙（DS&nbsp;Virgin Racing）、雷诺（Renault e.dams Racing team）和<a href="/car/0_0-0.0_0.0-0-115-0-0-0-0-1/" style="color:#157efb;" title="捷豹" target="_blank">捷豹</a>（Panasonic Jaguar Racing team），此外，参加Formula&nbsp;E还有两家电动车厂家：<a href="/car/0_0-0.0_0.0-0-143-0-0-0-0-1/" style="color:#157efb;" title="蔚来" target="_blank">蔚来</a>（NextEV NIO）和法拉第未来（<a href="/car/0_0-0.0_0.0-0-142-0-0-0-0-1/" style="color:#157efb;" title="Faraday Future" target="_blank">Faraday Future</a>）。</span> 
</p>
<p style="text-indent:2em;">
	<span style="font-size:16px;font-family:'Microsoft YaHei';">现在，Formula&nbsp;E已经开放了动力单元的使用和研发，各家车队根据各自情况不同，都有着不同的动力单元策略。更加开放的动力单元，也能让这些厂商车队展现出自己在能源领域科技水平的优势，尽管Formula&nbsp;E还有着诸多不足，但并不能掩盖其成为发展趋势的事实。在未来，一定会有更多的汽车厂商加入Formula&nbsp;E，只是早晚的问题。</span> 
</p>
